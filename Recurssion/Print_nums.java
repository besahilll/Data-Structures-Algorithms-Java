class Print_nums{
    public static void main(String[] args){
        //print_Decr(5);
        print_Inc(5);
        System.out.println();
        print_Decr(5);
    }

    public static void print_Decr(int n){  //Print numbers in decreasing order from 1 to n
        if(n==1){   //base condition
            System.out.println(n); 
            return ;
        }
        System.out.print(n+" ");
        print_Decr(n-1);
    }

    public static void print_Inc(int n){ //Print numbers in increasing order from 1 to n
        if(n==1){
            System.out.print(n+" ");
            return;
        }
        print_Inc(n-1);
        System.out.print(n+" ");
    }
}

